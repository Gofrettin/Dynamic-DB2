internal static class WowDatabaseHelper
{

    /// <summary>
    /// Get a string array of <see cref="WowDatabase"/> formatted for .db2 file type.>
    /// </summary>
    internal static string[] GetDb2Names()
    {
        var results = new string[(int)WowDatabase.Maximum];
        try
        {
            for (var i = 0; i < (int)WowDatabase.Maximum; i++)
                results[i] = ((WowDatabase)i).Db2String();
        }
        catch (Exception e) { Console.WriteLine(e); }
        return results;
    }

    /// <summary>
    /// Get a string array of <see cref="WowDatabase"/> formatted for .dbd file type.>
    /// </summary>
    internal static string[] GetDbdNames()
    {
        var results = new string[(int)WowDatabase.Maximum];
        try
        {
            for (var i = 0; i < (int)WowDatabase.Maximum; i++)
                results[i] = ((WowDatabase)i).DbdString();
        }
        catch (Exception e) { Console.WriteLine(e); }
        return results;
    }

    /// <summary>
    /// Convert a <see cref="WowDatabase"/> to a .db2 file <see cref="string"/> 
    /// </summary>
    internal static string Db2String(this WowDatabase wdb) 
        => $"{wdb.ToString().ToLower()}.db2";

    /// <summary>
    /// Convert a <see cref="WowDatabase"/> to a .dbd file <see cref="string"/> 
    /// </summary>
    internal static string DbdString(this WowDatabase wdb) 
        => $"{wdb}.dbd";
}